    //Méthode pour mettre a jour les boutons et le numéro 
    private void mettreAjourBoutonsEtNum() {
        if (compteur > listeQuestions.getSize()-1)
            compteur = 0;
        else if (compteur <0 )
            compteur = listeQuestions.getSize()-1;
        lblNumero.setText("(Question " + (compteur + 1) + "/" + listeQuestions.getSize() + ")");
        lblType.setText("<< "+listeQuestions.getQuestionN(listeQuestions.tableauCles()[compteur]).getTitre()+" >>");
    }

