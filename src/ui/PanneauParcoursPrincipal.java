    // charger un nouveau panneau
    private void chargerNouveauPanneau(){
        mettreAjourBoutonsEtNum();
        remove(monPanneau);
        monPanneau = GestionScenario.LancerPanneau(listeQuestions,listeQuestions.tableauCles()[compteur]);
        add(monPanneau);
        repaint();
    }
    
    //type suivant
    private void btnProchainTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProchainTypeActionPerformed
        String message = listeQuestions.getQuestionN(listeQuestions.tableauCles()[compteur]).getTitre();
        while(listeQuestions.getQuestionN(listeQuestions.tableauCles()[compteur]).getTitre().equals(message)){
            compteur++;
            mettreAjourBoutonsEtNum();
        }
        mettreAjourBoutonsEtNum();
        remove(monPanneau);
        monPanneau = GestionScenario.LancerPanneau(listeQuestions,listeQuestions.tableauCles()[compteur]);
        add(monPanneau);
        repaint(); 
    }//GEN-LAST:event_btnProchainTypeActionPerformed

