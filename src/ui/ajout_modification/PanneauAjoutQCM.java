    public void reinitialiser() {
        txtQuestion.setText("");
        for (int i = 0; i < listeChoix.size(); i++) {
            listeChoix.get(i).setText("");
            listeBonnesReponses.get(i).setSelected(false);
        }
    }

    public boolean validerData() {
        if (txtQuestion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "La question est obligatoire!",
                    "Saisie de valeurs", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        for (int i = 0; i < listeChoix.size(); i++) {
            if (listeChoix.get(i).getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Veuillez completer tous les champs manquants",
                        "Saisie de valeurs", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        }
        for (int i = 0; i < listeBonnesReponses.size(); i++) {
            if (listeBonnesReponses.get(i).isSelected()) {
                return true;
            }
        }

        JOptionPane.showMessageDialog(null, "Vous devez cocher au moins une bonne rÃ©ponse",
                "Choix des bonnes reponses", JOptionPane.ERROR_MESSAGE);
        return false;
    }


