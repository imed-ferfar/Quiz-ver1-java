    private void ajoutSupprimerChoix(int nombre) {
       
        //System.out.println(nombre);
        if (nombre > listeChoix.size()) {
            listeChoix.add(nouveauTextField());
            listeBonnesReponses.add(nouveauCheckBox());
        } else if (nombre < listeChoix.size()) {
            remove(listeChoix.get(listeChoix.size() - 1));
            listeChoix.remove(listeChoix.size() - 1);
            remove(listeBonnesReponses.get(listeBonnesReponses.size() - 1));
            listeBonnesReponses.remove(listeBonnesReponses.size() - 1);
        }
        repaint();
    }

